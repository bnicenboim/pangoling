#' Enriched  stimuli of `r RefManageR::Citet(mybib,"Frank2013")` data
#'
#' A dataset containing the stimuli used in `r RefManageR::Citet(mybib,"Frank2013")`. It is enriched with the estimates of surprisal and entropy reduction of `r RefManageR::Citet(mybib,"Frank2013a")`.
#'
#' As in the original dataset part-of-speech tags were generated by Tsuruoka & Tsujii's (2005) automatic tagger, and were corrected by hand (in accordance with the Penn Treebank part-of-speech  guidelines; Santorini, 1991); Punctuation marks receive their own tag, and clitics are split into two tags (e.g., "doesn't" is tagged "VBZ RB").
#'
#' @format A data frame with  rows and  variables:
#'
#' * `sent_id`: The sentence unique id.
#' * `sentence`: The sentence, as a single string
#' * `question`: Comprehension question (or hyphen if no question)
#' * `answer`: Correct answer to comprehension question (or hyphen if no question)
#' * `pos`: String of part-of-speech tags (Penn Treebank style)
#' * `word_n`: Position of the word in the sentence
#' * `word`: Presented word
#' * `surprisal_1`-`surprisal_10`: Surprisal estimate at ten points (x=1 to x=10) during network training.
#' * `deltaH1_1`-`deltaH4_10`: Entropy-reduction estimate at ten points during network training, with lookahead distance n.
#' @family datasets
#'
#' @references
#'
#' ```{r, echo = FALSE, results ="asis"}
#' print(pangoling:::mybib[c("Frank2013","Frank2013a")],.opts=list(style= "latex"))
#' ```
#' @seealso [data_frank2013_spr_complete] for the associated self-paced reading data, [data_frank2013_spr] for a cleaned version of that dataaset,  [data_frank2013_et_fix] for the eye-tracking version of this dataset with the fixation information, and [data_frank2013_et_rt] for a preprocessed eye-tracking dataset.
#
#' @source \url{https://osf.io/sjefs//}
"data_frank2013_stimuli"


##' Self-paced reading data of `r RefManageR::Citet(mybib,"Frank2013")`
#'
#' A dataset containing the self-paced reading data of `r RefManageR::Citet(mybib,"Frank2013")`. Notice that after collecting the self-paced-reading data, three typos were found:
#' - sentence 43  : "Scott" was "Sott"
#' - sentence 269 : "at" was "that"
#' - sentence 337 : "Margaret" was "Margeret"
#'
#' @format A data frame with  rows and  variables:
#'
#' * `subj`: Subject
#' * `sent_id`: Sentence unique id.
#' * `sent_n`: Position of sentence in presentation sequence
#' * `answer_time`: Time in msec between question presentation and response, or NaN if there was no question
#' * `word_n`: Position of the word in the sentence
#' * `word`: Presented word
#' * `RT`: Time in msec between word presentation and key press
#' * `acc_comprehension`: Correctness of response to comprehension question
#' * `age`: Subject's age in years
#' * `age_en`: Age at which subject began learning English (0 for native speakers)
#' * `sex`: Subject's sex (f/m)
#' * `hand`: Subject's handedness (r/l)
#' * `correct_perc`: Fraction of correct responses to comprehension questions.
#' * `typo`: Whether there is a typo in the word.
#' @references
#'
#' ```{r, echo = FALSE, results ="asis"}
#' print(pangoling:::mybib[c("Frank2013")],.opts=list(style= "latex"))
#' ```
#'
#' @family datasets
#' @seealso [data_frank2013_stimuli] for the enriched stimuli,  [data_frank2013_spr] for a cleaned version of this dataset,  [data_frank2013_et_fix] for the eye-tracking version of this dataset with the fixation information, and [data_frank2013_et] for a preprocessed eye-tracking dataset.
